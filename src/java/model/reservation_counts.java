package model;

import java.sql.Date;

/**
 *
 * @author admin
 */
public class reservation_counts {
    public Date date;
    public int count;

    public reservation_counts() {
    }

    public reservation_counts(Date date, int count) {
        this.date = date;
        this.count = count;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }
    
}
